public class ConfigProperties {
	private String uiURL;
	private String uploadDir;
	private Integer bufferSize;
	private Integer fileSize;
	private String lisencePath;
	
	/**
	 * @return the uiURL
	 */
	public String getUiURL() {
		return uiURL;
	}
	/**
	 * @param uiURL the uiURL to set
	 */
	public void setUiURL(String uiURL) {
		this.uiURL = uiURL;
	}
	/**
	 * @return the uploadDir
	 */
	public String getUploadDir() {
		return uploadDir;
	}
	/**
	 * @param uploadDir the uploadDir to set
	 */
	public void setUploadDir(String uploadDir) {
		this.uploadDir = uploadDir;
	}
	/**
	 * @return the bufferSize
	 */
	public Integer getBufferSize() {
		return bufferSize;
	}
	/**
	 * @param bufferSize the bufferSize to set
	 */
	public void setBufferSize(Integer bufferSize) {
		this.bufferSize = bufferSize;
	}
	/**
	 * @return the fileSize
	 */
	public Integer getFileSize() {
		return fileSize;
	}
	/**
	 * @param fileSize the fileSize to set
	 */
	public void setFileSize(Integer fileSize) {
		this.fileSize = fileSize;
	}
	/**
	 * @return the lisencePath
	 */
	public String getLisencePath() {
		return lisencePath;
	}
	/**
	 * @param lisencePath the lisencePath to set
	 */
	public void setLisencePath(String lisencePath) {
		this.lisencePath = lisencePath;
	}
}
